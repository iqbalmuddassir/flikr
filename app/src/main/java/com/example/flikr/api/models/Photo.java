package com.example.flikr.api.models;

public class Photo {
    private String id;
    private String secret;
    private String server;
    private String farm;
    private String title;

    public String getId() {
        return id;
    }

    public String getSecret() {
        return secret;
    }

    public String getServer() {
        return server;
    }

    public String getFarm() {
        return farm;
    }

    public String getTitle() {
        return title;
    }
}
